package data;

public class Lager {
	
	private int anz;
	private int per;
	private float kbindung;
	private float sammelbkosten;
	private float lagerVol;
	
	
	public Lager() {
		
	}


	/**
	 * @return the anz
	 */
	public int getAnz() {
		return anz;
	}


	/**
	 * @param anz the anz to set
	 */
	public void setAnz(int anz) {
		this.anz = anz;
	}


	/**
	 * @return the per
	 */
	public int getPer() {
		return per;
	}


	/**
	 * @param per the per to set
	 */
	public void setPer(int per) {
		this.per = per;
	}


	/**
	 * @return the kbindung
	 */
	public float getKbindung() {
		return kbindung;
	}


	/**
	 * @param kbindung the kbindung to set
	 */
	public void setKbindung(float kbindung) {
		this.kbindung = kbindung;
	}


	/**
	 * @return the sammelbkosten
	 */
	public float getSammelbkosten() {
		return sammelbkosten;
	}


	/**
	 * @param sammelbkosten the sammelbkosten to set
	 */
	public void setSammelbkosten(float sammelbkosten) {
		this.sammelbkosten = sammelbkosten;
	}


	/**
	 * @return the lagerVol
	 */
	public float getLagerVol() {
		return lagerVol;
	}


	/**
	 * @param lagerVol the lagerVol to set
	 */
	public void setLagerVol(float lagerVol) {
		this.lagerVol = lagerVol;
	}
	
	

}